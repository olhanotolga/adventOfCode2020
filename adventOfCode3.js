function countTrees(map, right, down) {
	const terrainLines = map.split("\n");
	const numOfLines = terrainLines.length;
	// const right = 3;
	// const down = 1;
	const tree = '#';
	const patternLength = terrainLines[0].length;

	let treeCount = 0;

	let xPos = 0;

	for (let yPos = 0; yPos < numOfLines; yPos += down) {
		let currentLine = terrainLines[yPos];
		// we're not counting trees on the starting line:
		if (yPos === 0) {
			continue;
		}
		// for each line, move 3 to the right and see if it's a tree.
		// if there is nowhere to move on the right (if there is no 31st el in the pattern, for example), then subtract the length to get the new index
		let newXPos = xPos + right;
		xPos = newXPos >= patternLength ? newXPos - patternLength : newXPos;
		if (currentLine[xPos] === tree) {
			// console.log(currentLine.substring(0, xPos+1));
			treeCount++;
		}

	}
	
	console.log(`Slope R${right}D${down}: ${treeCount}`);
	return treeCount;
}

